
DROP TABLE IF EXISTS vente_etablissementscolaire;
DROP TABLE IF EXISTS Vente_EspaceVert;
DROP TABLE IF EXISTS Vente;
DROP TABLE IF EXISTS SectionCadastrale;

DROP TABLE IF EXISTS Quartier;
CREATE TABLE Quartier (
    QuartierID INTEGER NOT NULL,
    NomQuartier VARCHAR(100),
    PrixImmobilier FLOAT,
    PRIMARY KEY (QuartierID)
);

COMMENT ON TABLE Quartier IS 'Table des 80 quartier';
COMMENT ON COLUMN Quartier.QuartierID IS 'ID du quartier';
COMMENT ON COLUMN Quartier.NomQuartier IS 'Nom du quartier';
COMMENT ON COLUMN Quartier.PrixImmobilier IS 'Prix au m2 selon notaire';

DROP TABLE IF EXISTS SectionCadastrale;
CREATE TABLE SectionCadastrale (
    SectionCadastraleID VARCHAR(5) NOT NULL,
    CodeCommune VARCHAR(3) NOT NULL,
    SectionCadastrale VARCHAR(2) NOT NULL,
    QuartierID INTEGER,
    EstTouristique BOOLEAN,
    PRIMARY KEY (SectionCadastraleID),
	FOREIGN KEY (QuartierID) REFERENCES Quartier(QuartierID)
);

COMMENT ON TABLE SectionCadastrale IS 'Table des sections cadastrales';
COMMENT ON COLUMN SectionCadastrale.SectionCadastraleID IS 'ID de la section';
COMMENT ON COLUMN SectionCadastrale.CodeCommune IS 'Code de l''arrondissement';
COMMENT ON COLUMN SectionCadastrale.SectionCadastrale IS 'Section cadastrale';
COMMENT ON COLUMN SectionCadastrale.EstTouristique IS 'Si la secgtion est touristique';
COMMENT ON COLUMN SectionCadastrale.QuartierID IS 'ID du Quartier';

DROP TABLE IF EXISTS Bois;

CREATE TABLE Bois(
	BoisID VARCHAR NOT NULL,
	NomBois VARCHAR,
	SurfaceBois FLOAT,
	ZoneElargie VARCHAR,
	PRIMARY KEY (BoisID)
);

DROP TABLE IF EXISTS EspaceVert;

CREATE TABLE EspaceVert(
	EspaceVertID VARCHAR NOT NULL,
	NomEspaceVert VARCHAR,
	TypeEspaceVert VARCHAR,
	CategorieEspaceVert VARCHAR,
	SurfaceEspaceVert FLOAT,
	CodePostal VARCHAR,
    ZoneElargie VARCHAR,
	PRIMARY KEY (EspaceVertID)
);

DROP TABLE IF EXISTS EtablissementScolaire;

CREATE TABLE EtablissementScolaire(
	EtablissementScolaireID VARCHAR NOT NULL,
	EtablissementScolaireName VARCHAR,
    Latitude FLOAT,
    Longitude FLOAT,
	PRIMARY KEY (EtablissementScolaireID)
);

DROP TABLE IF EXISTS ZoneRisque;

CREATE TABLE ZoneRisque(
	ZoneRisqueID VARCHAR NOT NULL,
	IntensiteRisque VARCHAR,
	GeoZone VARCHAR,
	PRIMARY KEY (ZoneRisqueID)
);

DROP TABLE IF EXISTS LoyerReference;

CREATE TABLE LoyerReference(
	LoyerReferenceID VARCHAR NOT NULL,
	Annee VARCHAR(4),
	Quartier VARCHAR,
	NombrePiece INTEGER,
	LoyerMoyenMeuble FLOAT,
	LoyerMoyenNonMeuble FLOAT,
	PRIMARY KEY (LoyerReferenceID)
);

DROP TABLE IF EXISTS Vente;

CREATE TABLE Vente (
	VenteID VARCHAR NOT NULL,
	ZoneRisqueID VARCHAR DEFAULT NULL,
	LoyerReferenceID VARCHAR DEFAULT NULL,
	BoisID VARCHAR DEFAULT NULL,
	DateVente DATE,
	ValeurFonciere FLOAT,
	NumeroVoie VARCHAR,
	TypeVoie VARCHAR,
	Voie VARCHAR,
	CodePostal VARCHAR,
	Commune VARCHAR,
	SectionCadastraleID VARCHAR NOT NULL,
	NombrePiece INTEGER,
	SurfaceReelle FLOAT,
	NombreDependance INTEGER,
	Latitude FLOAT,
	Longitude FLOAT,
	ZoneElargie VARCHAR,
	PRIMARY KEY (VenteID),
    FOREIGN KEY (ZoneRisqueID) REFERENCES ZoneRisque(ZoneRisqueID),
    FOREIGN KEY (LoyerReferenceID) REFERENCES LoyerReference(LoyerReferenceID),
    FOREIGN KEY (BoisID) REFERENCES Bois(BoisID),
    FOREIGN KEY (SectionCadastraleID) REFERENCES SectionCadastrale(SectionCadastraleID)
);

DROP TABLE IF EXISTS Vente_EtablissementScolaire;

CREATE TABLE Vente_EtablissementScolaire (
    VenteID VARCHAR NOT NULL,
    EtablissementScolaireID VARCHAR NOT NULL,
    PRIMARY KEY (VenteID, EtablissementScolaireID),
    FOREIGN KEY (VenteID) REFERENCES Vente(VenteID),
    FOREIGN KEY (EtablissementScolaireID) REFERENCES EtablissementScolaire(EtablissementScolaireID)
);

DROP TABLE IF EXISTS Vente_EspaceVert;

CREATE TABLE Vente_EspaceVert(
	VenteID VARCHAR NOT NULL,
	EspaceVertID VARCHAR NOT NULL,
	PRIMARY KEY (VenteID, EspaceVertID),
	FOREIGN KEY (VenteID) REFERENCES Vente(VenteID),
    FOREIGN KEY (EspaceVertID) REFERENCES EspaceVert(EspaceVertID)
);

